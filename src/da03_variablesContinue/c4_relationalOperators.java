package da03_variablesContinue;

public class c4_relationalOperators {



    public static void main(String[] Args){

     // first we create variables which is 2 numbers

        byte b1=10;
        byte b2=25;

        boolean example=b1==b2; // false



        //==equal operator

        System.out.println(example);

        //!= (opposite of equal) not equal

        System.out.println(b1!=b2); // true

        // > operator
        System.out.println(b1>b2); // false


        //< operator
        System.out.println(b1<b2); //true

        // >= operator bigger or equal
        System.out.println(10>=11); // false

        // <= operator small or equal
        System.out.println(10<=15); // true












    }
}
